package Day_StudyHall;

public class Study_1013One {
    public static void main(String[] args) {
        int month = 8;
        String monthString;

        switch (month){

            case 1:
                monthString = "January";
                System.out.println("January");
                break;
            case 2:
                monthString = "Feb";
                System.out.println("February");
                break;
            case 3:
                monthString = "March";
                System.out.println("March");
                break;
            case 4:
                monthString = "April";
                System.out.println("April");
                break;

        }
    }// end main
}//end class
